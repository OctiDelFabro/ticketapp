export const formatPrice = (price) =>
  new Intl.NumberFormat('es-AR', {
    style: 'currency',
    currency: 'ARS',
    maximumFractionDigits: 0,
  })
    .format(price)
    .replace('ARS', '$')
    .replace(/\s/g, '')

export const serviceFee = (price, quantity = 1) => Math.round(price * quantity * 0.1)

